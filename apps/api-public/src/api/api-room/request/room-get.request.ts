import { ApiPropertyOptional, IntersectionType, PickType } from '@nestjs/swagger'
import { Expose, Transform, plainToInstance } from 'class-transformer'
import { IsObject, ValidateNested } from 'class-validator'
import { LimitQuery, PaginationQuery } from '../../../../../_libs/common/dto/query'
import {
  RoomFilterQuery,
  RoomRelationQuery,
  RoomSortQuery,
} from './room-options.request'

export class RoomGetQuery {
  @ApiPropertyOptional({ type: String, example: JSON.stringify(<RoomRelationQuery>{}) })
  @Expose()
  @Transform(({ value }) => {
    try {
      if (!value) return undefined // return undefined để không validate nữa
      const plain = JSON.parse(value)
      return plainToInstance(RoomRelationQuery, plain, {
        exposeUnsetFields: false,
        excludeExtraneousValues: false, // không bỏ qua field thừa, để validate chết nó
      })
    } catch (error) {
      return error.message
    }
  })
  @IsObject({ message: ({ value }) => value })
  @ValidateNested({ each: true })
  relation: RoomRelationQuery

  @ApiPropertyOptional({
    type: String,
    example: JSON.stringify(<RoomFilterQuery>{ updatedAt: { GT: Date.now() } }),
  })
  @Expose()
  @Transform(({ value }) => {
    try {
      if (!value) return undefined // return undefined để không validate nữa
      const plain = JSON.parse(value)
      return plainToInstance(RoomFilterQuery, plain, {
        exposeUnsetFields: false,
        excludeExtraneousValues: false, // không bỏ qua field thừa, để validate chết nó
      })
    } catch (error) {
      return error.message
    }
  })
  @IsObject({ message: ({ value }) => value })
  @ValidateNested({ each: true })
  filter?: RoomFilterQuery

  @ApiPropertyOptional({
    type: String,
    example: JSON.stringify(<RoomSortQuery>{ id: 'ASC' }),
  })
  @Expose()
  @Transform(({ value }) => {
    try {
      if (!value) return undefined // return undefined để không validate nữa
      const plain = JSON.parse(value)
      return plainToInstance(RoomSortQuery, plain, {
        exposeUnsetFields: false,
        excludeExtraneousValues: false, // không bỏ qua field thừa, để validate chết nó
      })
    } catch (error) {
      return error.message
    }
  })
  @IsObject({ message: ({ value }) => value })
  @ValidateNested({ each: true })
  sort?: RoomSortQuery
}

export class RoomPaginationQuery extends IntersectionType(
  RoomGetQuery,
  PaginationQuery
) { }

export class RoomGetManyQuery extends IntersectionType(
  PickType(RoomGetQuery, ['filter', 'relation', 'sort']),
  LimitQuery
) { }

export class RoomGetOneQuery extends PickType(RoomGetQuery, ['relation']) { }
