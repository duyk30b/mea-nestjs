import { ApiPropertyOptional, IntersectionType, PickType } from '@nestjs/swagger'
import { Expose, Transform, plainToInstance } from 'class-transformer'
import { IsObject, ValidateNested } from 'class-validator'
import { LimitQuery, PaginationQuery } from '../../../../../_libs/common/dto/query'
import {
  ExpenseFilterQuery,
  ExpenseRelationQuery,
  ExpenseSortQuery,
} from './expense-options.request'

export class ExpenseGetQuery {
  @ApiPropertyOptional({ type: String, example: JSON.stringify(<ExpenseRelationQuery>{}) })
  @Expose()
  @Transform(({ value }) => {
    try {
      if (!value) return undefined // return undefined để không validate nữa
      const plain = JSON.parse(value)
      return plainToInstance(ExpenseRelationQuery, plain, {
        exposeUnsetFields: false,
        excludeExtraneousValues: false, // không bỏ qua field thừa, để validate chết nó
      })
    } catch (error) {
      return error.message
    }
  })
  @IsObject({ message: ({ value }) => value })
  @ValidateNested({ each: true })
  relation: ExpenseRelationQuery

  @ApiPropertyOptional({
    type: String,
    example: JSON.stringify(<ExpenseFilterQuery>{ printHtmlId: 0 }),
  })
  @Expose()
  @Transform(({ value }) => {
    try {
      if (!value) return undefined // return undefined để không validate nữa
      const plain = JSON.parse(value)
      return plainToInstance(ExpenseFilterQuery, plain, {
        exposeUnsetFields: false,
        excludeExtraneousValues: false, // không bỏ qua field thừa, để validate chết nó
      })
    } catch (error) {
      return error.message
    }
  })
  @IsObject({ message: ({ value }) => value })
  @ValidateNested({ each: true })
  filter?: ExpenseFilterQuery

  @ApiPropertyOptional({
    type: String,
    example: JSON.stringify(<ExpenseSortQuery>{ id: 'ASC' }),
  })
  @Expose()
  @Transform(({ value }) => {
    try {
      if (!value) return undefined // return undefined để không validate nữa
      const plain = JSON.parse(value)
      return plainToInstance(ExpenseSortQuery, plain, {
        exposeUnsetFields: false,
        excludeExtraneousValues: false, // không bỏ qua field thừa, để validate chết nó
      })
    } catch (error) {
      return error.message
    }
  })
  @IsObject({ message: ({ value }) => value })
  @ValidateNested({ each: true })
  sort?: ExpenseSortQuery
}

export class ExpensePaginationQuery extends IntersectionType(
  ExpenseGetQuery,
  PaginationQuery
) { }

export class ExpenseGetManyQuery extends IntersectionType(
  PickType(ExpenseGetQuery, ['filter', 'relation', 'sort']),
  LimitQuery
) { }

export class ExpenseGetOneQuery extends PickType(ExpenseGetQuery, ['relation']) { }
