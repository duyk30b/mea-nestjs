import { ApiPropertyOptional, IntersectionType, PickType } from '@nestjs/swagger'
import { Expose, Transform, plainToInstance } from 'class-transformer'
import { IsObject, ValidateNested } from 'class-validator'
import { LimitQuery, PaginationQuery } from '../../../../../_libs/common/dto/query'
import {
  DiscountFilterQuery,
  DiscountRelationQuery,
  DiscountSortQuery,
} from './discount-options.request'

export class DiscountGetQuery {
  @ApiPropertyOptional({ type: String, example: JSON.stringify(<DiscountRelationQuery>{}) })
  @Expose()
  @Transform(({ value }) => {
    try {
      if (!value) return undefined // return undefined để không validate nữa
      const plain = JSON.parse(value)
      return plainToInstance(DiscountRelationQuery, plain, {
        exposeUnsetFields: false,
        excludeExtraneousValues: false, // không bỏ qua field thừa, để validate chết nó
      })
    } catch (error) {
      return error.message
    }
  })
  @IsObject({ message: ({ value }) => value })
  @ValidateNested({ each: true })
  relation: DiscountRelationQuery

  @ApiPropertyOptional({
    type: String,
    example: JSON.stringify(<DiscountFilterQuery>{}),
  })
  @Expose()
  @Transform(({ value }) => {
    try {
      if (!value) return undefined // return undefined để không validate nữa
      const plain = JSON.parse(value)
      return plainToInstance(DiscountFilterQuery, plain, {
        exposeUnsetFields: false,
        excludeExtraneousValues: false, // không bỏ qua field thừa, để validate chết nó
      })
    } catch (error) {
      return error.message
    }
  })
  @IsObject({ message: ({ value }) => value })
  @ValidateNested({ each: true })
  filter?: DiscountFilterQuery

  @ApiPropertyOptional({
    type: String,
    example: JSON.stringify(<DiscountSortQuery>{ id: 'ASC' }),
  })
  @Expose()
  @Transform(({ value }) => {
    try {
      if (!value) return undefined // return undefined để không validate nữa
      const plain = JSON.parse(value)
      return plainToInstance(DiscountSortQuery, plain, {
        exposeUnsetFields: false,
        excludeExtraneousValues: false, // không bỏ qua field thừa, để validate chết nó
      })
    } catch (error) {
      return error.message
    }
  })
  @IsObject({ message: ({ value }) => value })
  @ValidateNested({ each: true })
  sort?: DiscountSortQuery
}

export class DiscountPaginationQuery extends IntersectionType(DiscountGetQuery, PaginationQuery) { }

export class DiscountGetManyQuery extends IntersectionType(
  PickType(DiscountGetQuery, ['filter', 'relation', 'sort']),
  LimitQuery
) { }

export class DiscountGetOneQuery extends PickType(DiscountGetQuery, ['relation']) { }
